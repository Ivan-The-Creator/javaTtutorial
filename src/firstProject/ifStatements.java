package firstProject;

public class ifStatements {
	
	public static void main (String[] args) {
		
		int age = 16;
		
		if (age >= 75) {
			System.out.println("You are an adult!");
		} else if (age >= 18) {
			System.out.println("You are an adult!");
		} else {
			System.out.println("You are a teenager!");
		}
		
		
		if (age == 26) {
			System.out.println(true);
		} else {
			System.out.println(false);
		}
		
	}
	
}